function calcolator(numberOne, numberTwo, operator) {

    switch (operator) {
        case 'multiply':
            console.log(numberOne * numberTwo)
            break;
    
        case 'divide':
            console.log(numberOne / numberTwo)
            
            break;
        case 'add':
            console.log(numberOne + numberTwo)
            
            break;

        case 'subtract':
            console.log(numberOne - numberTwo)
            
            break;
    }

    
}